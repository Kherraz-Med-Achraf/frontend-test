import { useSelector, useDispatch } from 'react-redux';
import './styles.scss';
import { setSelectedTimeControl } from '../../DataManagement/reducers/timeControlsReducer';
// ---------------------------------------------------------------------------------------------------------------------

const List = () => {
	const dispatch = useDispatch();
	const timeControls = useSelector(
		(state) => state.timeControlsReducer.timeControls
	);
	const handleSelectTimeControl = (x) => {
		dispatch(setSelectedTimeControl(x));
		return;
	};
	return (
		<div className='list'>
			Liste :{' '}
			{timeControls.map((x) => (
				<button onClick={() => handleSelectTimeControl(x)}>{x}</button>
			))}
		</div>
	);
};

export default List;
