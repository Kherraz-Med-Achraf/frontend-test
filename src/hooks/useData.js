// -------------------------------------------------- React & redux ----------------------------------------------------
import { useMemo } from 'react';
import { useGetTopPlayersQuery } from '../DataManagement/services/chess';
// ---------------------------------------------------------------------------------------------------------------------

// --------------------------------------------------- Redux State Management ------------------------------------------

// ---------------------------------------------------------------------------------------------------------------------

// --------------------------------------------------- Utils -----------------------------------------------------------

// ---------------------------------------------------------------------------------------------------------------------

export const useData = () => {
	const { data, isLoading, isError } = useGetTopPlayersQuery();

	const rankByTimeControl = useMemo(() => {
		return data && Object.keys(data).map((key) => [key, data?.[key]]);
	}, [data]);

	return { rankByTimeControl, isLoading, isError };
};
